    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        
        for (Departamento listaDepartamento : listaDepartamentos) {
            if (listaDepartamento.getCodigo()==Double.parseDouble(txtCodigo.getText())) {
                for (Propietario propietario: listaPropietarios) {
                    if(propietario.getCodigoDeDepartamento()==Double.parseDouble(txtCodigo.getText())&&
                            (propietario.getUsuario().equals(txtUsuario.getText()))&&
                                (propietario.getContrasenia().equals(txtContrasena.getText()))){
                        labelAcceso.setText("!Acceso Concedido!");
                        labelAcceso.setForeground(Color.green);
                        break;
                    } else{
                        labelAcceso.setText("!Acceso Denegado!");
                        labelAcceso.setForeground(Color.red);
                    }
                }
            }
            break;
        }
    }//GEN-LAST:event_botonIngresarActionPerformed

