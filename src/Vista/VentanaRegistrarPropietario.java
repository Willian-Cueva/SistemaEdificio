    private void txtCedulaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedulaKeyReleased
        Pattern patron;
        patron = Pattern.compile("[A-Za-z0-9]+$");

        String contra = txtCedula.getText();
        Matcher mat = patron.matcher(contra);

        if (mat.find() && contra.length() == 10) {
            labelMensajeCedula.setText("*Cedula Valida");
            cedulaCorrecta = true;
        } else {
            labelMensajeCedula.setText("*Cedula Invalida");
            cedulaCorrecta = false;
        }
    }//GEN-LAST:event_txtCedulaKeyReleased

