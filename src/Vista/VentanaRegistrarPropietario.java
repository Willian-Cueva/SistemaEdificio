    private void botonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarActionPerformed
        if (cedulaCorrecta) {
            Propietario p = new Propietario();
            p.setCedula(txtCedula.getText());
            p.setUsuario(txtUsuario.getText());
            p.setContrasenia(txtContra.getText());
            p.setCodigoDeDepartamento((int) listaDepartamentos.get(seleccion).getCodigo());
            String[] dato = new String[2];
            dato[0] = txtUsuario.getText();
            dato[1] = txtCedula.getText();

            tabla.addRow(dato);

            File archivo = new File("Propietarios.txt");
            String cadena = (p.getCodigoDeDepartamento() + "," + p.getContrasenia() + "," + p.getUsuario() + "," + p.getCedula() + "\n");
            try {
                FileWriter fw = new FileWriter(archivo, true);
                fw.write(cadena);
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(VentanaRegistrarPropietario.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtCedula.setText(null);
            txtUsuario.setText(null);
            txtCedula.isFocusable();
            lleno();

        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar correctamente la cedula");
        }
        labelNodePropietarios.setText("No de Propietarios Ingresados: " + tabla.getRowCount());
        lleno();

    }//GEN-LAST:event_botonIngresarActionPerformed

