    public void actualizarCodigo(){
        if (listaDepartamentos.size()>0) {
            codigoLabel =(int) listaDepartamentos.get(listaDepartamentos.size()-1).getCodigo();
        ++codigoLabel;
        labelCodigodef.setText(String.valueOf(codigoLabel));
        }else if(listaDepartamentos.size()==0){
            codigoLabel=1;
            labelCodigodef.setText(String.valueOf(codigoLabel));
        }
    }

