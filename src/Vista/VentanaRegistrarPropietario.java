    private boolean archivoEditable() {
        File prop = new File("Propietarios.txt");
        if (prop.length() != 0) {
            return true;
        } else {
            return false;
        }
    }

    public void agragarPropietarios() {
        if (archivoEditable()) {
            double codigoDelDepartamento = listaDepartamentos.get(seleccion).codigo;
            if (archivoEditable()) {
                listaPropietarios = crtlDepartamento.cargarListaPropietarios();
            }
            listaPropietarios = crtlDepartamento.cargarListaPropietarios();

            String contrasena = listaDepartamentos.get(seleccion).getContraseniaDepartamento();
            if (archivoEditable()) {
                for (Propietario propietario : listaPropietarios) {
                    if ((codigoDelDepartamento == propietario.getCodigoDeDepartamento()) && (contrasena.equals(propietario.getContrasenia())) || listaPropietarios.size() == 0) {
                        String[] dato = new String[2];
                        dato[0] = propietario.getUsuario();
                        dato[1] = propietario.getCedula();

                        tabla.addRow(dato);

                        ++contadorPropietarios;
                    }
                }
            }
            lleno();
        }
    }

    public void lleno() {
        if (archivoEditable()) {
            if (listaDepartamentos.get(seleccion).getCapacidad() == tabla.getColumnCount()) {
                labelRegistrarProoietario.setText(null);
                labelRegistrarProoietario.setText("DEPARTAMENTO LLENO");
                txtCedula.enable(false);
                txtUsuario.enable(false);
                botonIngresar.setVisible(false);
            }
        }

    }

