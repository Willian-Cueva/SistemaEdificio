    private void botonIngresarDepartementoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresarDepartementoActionPerformed
        departamento=new Departamento();
        String[] datos= new String[4];
        datos[0]=labelCodigodef.getText();
        datos[1]=txtContrasena.getText();
        datos[2]=txtNombreDepartamento.getText();
        datos[3]=txtCapacidad.getText();
        tablaGeneral.addRow(datos);
        double capacidad=Double.valueOf(datos[3]);
        String filaString=(datos[0]+","+datos[1]+","+datos[2]+","+datos[3]+"\n");
        try {
            FileWriter fr = new FileWriter(archivoDepartamentos.getAbsoluteFile(),true);
            fr.write(filaString);
            fr.close();
            
            departamento.setCodigo(Double.valueOf(datos[0]));
            departamento.setContraseniaDepartamento(datos[1]);
            departamento.setNombreDepartamento(datos[2]);
            departamento.setCapacidad(capacidad);
            listaDepartamentos.add(departamento);
            actualizarCodigo();
        } catch (IOException ex) {
            Logger.getLogger(VentanaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
        }
//        txtCodigo.setText(null);
        txtContrasena.setText(null);
        txtNombreDepartamento.setText(null);
        txtCapacidad.setText(null);
        txtNumeroPiso.setText(null);
    }//GEN-LAST:event_botonIngresarDepartementoActionPerformed

