    public void cargarDepartamentos(){
        for (Departamento listaDepartamento : listaDepartamentos) {
            String[] datos=new String[4];
            datos[0]=String.valueOf(listaDepartamento.getCodigo());
            datos[1]=listaDepartamento.getContraseniaDepartamento();
            datos[2]=listaDepartamento.getNombreDepartamento();
            datos[3]=String.valueOf(listaDepartamento.getCapacidad());
            tablaGeneral.addRow(datos);
        }
    }

